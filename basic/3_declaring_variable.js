/**
 * Variable 선언
 *
 * 1) var - 사용 X
 * 2) let
 * 3) const
 */

var name = "juhyeong";
console.log(name);

var age = 4;
console.log(age);

let ive = "아이브";
console.log(ive);

/**
 * let, var로 선언시
 * 값을 추후에 변경 가능.
 */

ive = "안유진";
console.log(ive);

const newjeans = "뉴진스";
console.log(newjeans);

// newjeans = 'juhyeong';
// var, let처럼 변경 안됨

